            string faultmessage = null;

            if ((group.Courses.Count() != 0) || (group.Users.Count() != 0)) {

                faultmessage = "Gruppen kan ej tas bort då den innehåller kopplingar till användare eller kurs.";

                TempData["Faultmessage"]=faultmessage;
